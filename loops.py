# LOOPS
# while loops

number = 20
while number <= 25:
    print(number)
    number += 1

# decrementing values

a = 10
while a >= 1:
    print(a)
    a -= 1

# for loop

languages = ["python", "php", "kotlin", "C++"]
for x in languages:
    print(x)

# Range

for x in range(10):
    print(x)

for a in range(15 - 21):
    print(a)

for c in range(4, 30, 2):
    print(c)